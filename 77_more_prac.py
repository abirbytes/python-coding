#WAP to find the sum of first n numbers (using while loop)
n=3
sum=0
i=1
while i<=n:
   sum+=i
   i+=1
print(sum)

#for loop
x=3
sum=0
for i in range(x+1):
   sum+=i
print(sum)

n=4
product = 1
for i in range(1,n+1):
   product*=i
print(f"The factorial of {n} is {product}")

